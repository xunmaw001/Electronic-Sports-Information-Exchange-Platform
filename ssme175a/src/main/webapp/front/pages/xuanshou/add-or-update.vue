		data() {
			return {
				ruleForm: {
												mingcheng: '',
																suoshuzhandui: '',
																zhaopian: '',
																dangqianduanwei: '',
																dangqianfenshu: '',
																jinqibisaijilu: '',
																xiangqing: '',
																thumbsupnum: '',
																crazilynum: '',
												},
																																				dangqianduanweiOptions: [],
				dangqianduanweiIndex: 0,
																																																				// 登陆用户信息
				user: {},
                                ro:{
                                   mingcheng : false,
                                   suoshuzhandui : false,
                                   zhaopian : false,
                                   dangqianduanwei : false,
                                   dangqianfenshu : false,
                                   jinqibisaijilu : false,
                                   xiangqing : false,
                                   thumbsupnum : false,
                                   crazilynum : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																												
																														// 自定义下拉框值
